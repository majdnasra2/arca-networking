def deltas(tsc):
    return [tsc[i+1] - tsc[i] for i in range(len(tsc)-1)]

def average_runs(runs):
    all_d = [deltas(r) for r in runs]
    n = len(all_d)
    m = len(all_d[0])
    return [sum(run[i] for run in all_d) / n for i in range(m)]

def total_diff(tsc):
    return tsc[-1] - tsc[0]

# =========================
# COMPUTE
# =========================
